		<h3><a class="heading" name="main-menu-review">3. Обзор главного меню</a></h3>

		<p class="text"> Главное меню студента представляет собой верхнее горизонтальное меню с пунктами <b>Главная</b>, <b>Связаться с разработчиком</b>, <b>Журнал событий</b>, <b>Правила пользования</b> и выпадающее меню личного профиля студента <b>Добро пожаловать ИМЯ ОТЧЕСТВО</b>, а, также, в заголовке главного меню в указано название направления и номер группы авторизовавшегося студента.</p>
		<p class="text"> В центральной области главного меню представлены два окна с выбором, соответственно, типа экзаменовок и темы экзаменовок определенного предмета. </p>
		<img class="img-for-text" src="/img/2pSN-wARGH4.jpg">
		<p class="text-for-img">Рис.3.1 Главное меню студента</p>
		<p class="text"> В разделе типов экзаменовок представленны следующие варианты выбора:</p>
			<p class="second-text">
				<b>Демо примеры</b> - демонстрационный материал, специально подготовленный преподавателем как один из вариантов ведения лекций или семинаров. Представляет из себя комбинацию текста с соответствующими изображениями; не имеет вариантов ответа.  Демо пример не считается как отдельный вид экзаменовкии и его прохождение не отражается в личной статистике студента.
			</p>
			<p class="second-text">
				<b>Тестирование</b> - один из видов экзаменовок, предназначенный для проверки теоретических знаний студентов. Представляет из себя вопросы с выбором одного или нескольких вариантов ответов из предложенных. 
			</p>
			<p class="second-text">
				<b>Практика</b> - один из видов экзаменовок, предназначенный для проверки практических умений студентов. Представляет из себя вопросы, ответы на которые вводятся в специальные поля с клавиатуры. 
			</p>
			<p class="second-text">
				<b>Проверочная работа</b> - контрольный вид экзаменовки, представляющий из себя комбинацию теоретических и практических вопросов и ограниченный по времени 20 (двадцать) минут.
			</p>

