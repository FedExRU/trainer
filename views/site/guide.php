		<h3><a class="heading" name="examination">4. Экзаменация</a></h3>
		<p class="text"> Экзаменация - прохождение в сервисе одного из представленных типов экзаменовок: тестирования, практики или проверочной работы. При прохождении экзаменации каждому варианту генерируется уникальный номер вида <b>A123-a-b-c-dd-mm-yy-xxxxxx</b>, где: </p>
			<p class="second-text"> <b>A</b> - первая буква типа экзаменовки: T - тестирование, P - практика, E - проверочная работа</p> <br>
			<p class="second-text"> <b>123</b> - порядковый номер записи экзаменовки в базе данных</p><br>
			<p class="second-text"> <b>a</b> - идентификатор типа экзаменовки </p><br>
			<p class="second-text"> <b>b</b> - идентификатор предмета</p><br>
			<p class="second-text"> <b>c</b> - идентификатор темы предмета</p><br>
			<p class="second-text"> <b>dd</b> - день экзаменовки </p><br>
			<p class="second-text"> <b>mm</b> - месяц экзаменовки</p><br>
			<p class="second-text"> <b>yy</b> - последние две цифры года экзаменовки</p><br>
			<p class="second-text"> <b>xxxxxx</b> - случайно сгенерированные числа</p><br>

		<p class="text"> При просмотре демо примеров номер варианта не генерируется. Результат теста сохраняется в личной статистике пользователя и отражается в его личном профиле. </p>		

